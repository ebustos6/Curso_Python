# -*- coding: utf-8 -*-

s1 = "Prueba"
s2 = "prueba"
s3 = " prueba "
s4 = "Hola Mundo"

# Capitalizar cadena de texto
s2.capitalize()
# Convertir en minuscula todos los caracteres de la cadena de caracteres.
s1.lower()
# Convertir en mayuscula todos los caracteres de la cadena de caracteres.
s2.upper()
# Sacar los espacios al comienzo y al final de la cadena de caracteres.
s3.strip()
# Dividir la cadena de caracteres según la cadena de caracteres pasado como parámetro.
s4.split(" ")
# Intercambia las mayusculas por minusculas y viceversa.
s1.swapcase()
# Le pone formato de titulo.
s2.title()
# Quita los espacios en blanco de la derecha.
s3.rstrip()
# Quita los espacios en blanco de la izquierda.
s3.lstrip()
# Justifica la cadena de texto a la derecha con una longitud de tantos caracteres como se indica en el parámetro.
s1.rjust(10)
# Justifica la cadena de texto a la izquierda con una longitud de tantos caracteres como se indica en el parámetro.
s1.ljust(10)
# Reemplaza una cadena de texto por otra en la cadena de texto.
s2.replace("r", "h")
# Centra la cadena de texto tomando como longitud la cantidad de caracteres indicada como parámetro.
s1.center(10)
# Completa la cadena de texto con ceros tomando como longitud la cantidad de caracteres indicada como parámetro.
s1.zfill(10)

